// we need 2 loop(for)
star = '*'

// first loop ; take star (type: string) 
for (let i = 1; i <= 3 ; i++){
    // The number of output stars by -for-
    console.log(star.repeat(i));
    // with -repeat(string method)- give more stars
}

// second loop ; take star (type: string) 
for (let i = 2; i >= 1 ; i--){
    // The number of output stars by -for-
    console.log(star.repeat(i));
    // with -repeat(string method)- give more stars
}
